package de.qbyte.faultchecker.exception;

public class Exception3 extends RuntimeException {

	private static final long serialVersionUID = -9107963395981413849L;

	public Exception3() {
		super();
	}

	public Exception3(String message, Throwable cause) {
		super(message, cause);
	}

	public Exception3(String message) {
		super(message);
	}

	public Exception3(Throwable cause) {
		super(cause);
	}

}
